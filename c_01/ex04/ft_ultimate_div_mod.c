void	ft_ultimate_div_mod(int *a, int *b)
{
	int	aux1;
	int	aux2;

	if (*b != 0)
	{
		aux1 = *a / *b;
		aux2 = *a % *b;
		*a = aux1;
		*b = aux2;
	}
}
